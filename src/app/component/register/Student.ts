export class Student{
    name:string;
    dept:string;
    email:string;
    phone:string;
    course:string;
    constructor(name:string,dept:string,email:string,phone:string,course:string)
    {
        this.name=name;
        this.dept=dept;
        this.email=email;
        this.phone=phone;
        this.course=course;
    }
}